    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        // TODO add your handling code here:

        String pid = txtpid.getText();
        String pname = txtpname.getText();
        String pprice = txtpprice.getText();
        String pstock = txtpstock.getText();

        try {
            Class.forName("com.mysql.jdbc.Driver");
            con =(Connection) DriverManager.getConnection("jdbc:mysql://localhost/stock_mgt","root","");
            pst =(PreparedStatement) con.prepareStatement("insert into pr_reg(product_id,product_name,product_price,stock)values(?,?,?,?)");
            pst.setString(1, pid);
            pst.setString(2, pname);
            pst.setString(3, pprice);
            pst.setString(4, pstock);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Catogory Added");
            table_update();

            txtpid.setText("");
            txtpname.setText("");
            txtpprice.setText("");
            txtpstock.setText("");

            txtpid.requestFocus();

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(stock_release.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_addbtnActionPerformed

