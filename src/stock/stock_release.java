    private void table_update()
    {
        try {
            int c;
            try {
                Class.forName("com.mysql.jdbc.Driver");
                con =(Connection) DriverManager.getConnection("jdbc:mysql://localhost/stock_mgt","root","");
                pst = (PreparedStatement) con.prepareStatement("select * from pr_reg");
                ResultSet rs =pst.executeQuery();
                
                ResultSetMetaData rsd = (ResultSetMetaData) rs.getMetaData();
                c = rsd.getColumnCount();
                
                DefaultTableModel d = (DefaultTableModel)ptable.getModel();
                d.setRowCount(0);
                              
                while(rs.next())
                {
                    Vector v2 = new  Vector();
                  
                        for(int i=1; i<=c; i++)
                        {   
                            v2.add(rs.getInt("id"));
                            v2.add(rs.getString("product_id"));
                            v2.add(rs.getString("product_name"));                            
                            v2.add(rs.getDouble("product_price"));
                            v2.add(rs.getInt("stock"));
                            
                        }
                        
                        d.addRow(v2);                   
                }                                                    
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(stock_release.class.getName()).log(Level.SEVERE, null, ex);
            }                      
        } catch (SQLException ex) {
            Logger.getLogger(stock_release.class.getName()).log(Level.SEVERE, null, ex);
        }            
    }

    private void editbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editbtnActionPerformed
        
        DefaultTableModel d = (DefaultTableModel)ptable.getModel();
        int selectIndex = ptable.getSelectedRow();
        
        int id = Integer.parseInt(d.getValueAt(selectIndex,0).toString());         
        String pid = txtpid.getText();                 
        String pname = txtpname.getText();
        String pprice = txtpprice.getText();
        String pstock = txtpstock.getText();
        
            try {
            Class.forName("com.mysql.jdbc.Driver");
            con =(Connection) DriverManager.getConnection("jdbc:mysql://localhost/stock_mgt","root","");
            pst =(PreparedStatement) con.prepareStatement("update pr_reg set product_id=? , product_name=? , product_price=? , stock=? where id=? ");
            
            pst.setString(1, pid);
            pst.setString(2, pname);
            pst.setString(3, pprice);
            pst.setString(4, pstock);
            pst.setInt(5, id);
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Catogory Updated");
            table_update();
                          
            txtpid.setText("");
            txtpname.setText("");
            txtpprice.setText("");
            txtpstock.setText("");
            
            txtpid.requestFocus();
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(stock_release.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_editbtnActionPerformed

